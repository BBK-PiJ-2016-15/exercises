public class Calculator {
	private double a;
	private double b;


	public double add(double a, double b){
		return a + b
	}
	public double subtraction(a,b){
		return a - b
	}
	public double multi(a,b){
		return a * b
	}
	public double div(a,b) {
		return a/b
	}

}

Calculator calc = new Calculator()

println calc.add(23,3)
println calc.subtraction(23,3)
println calc.multi(23,3)
println calc.div(23,3)
